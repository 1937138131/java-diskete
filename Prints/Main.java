public class Main {
    public static void main(String[] args) {
        // Exemplo de System.out.print
        System.out.print("Olá, ");
        System.out.print("mundo!");
        System.out.println(); // Pula para a próxima linha

        // Exemplo de System.out.println
        System.out.println("Olá, mundo!");
        System.out.println("Java!");

        // Exemplo de concatenação com +
        System.out.println("O resultado é: " + 10);

        // Exemplo de printf com formatação
        System.out.printf("Nome: %s, Idade: %d anos\n", "João", 25);

        // Exemplo de escape characters
        System.out.print("Linha 1\nLinha 2\tcom tab");
    }
}
